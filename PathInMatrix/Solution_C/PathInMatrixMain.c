double validate(int* result, int (*algo)(int[20][20], int, int), int X[20][20], int n, int m){
    clock_t  start = clock();
    *result = algo(X, n, m);
    clock_t  end = clock();
    double duration_s = (double)(end - start) / CLOCKS_PER_SEC;
    double duration_ms = duration_s * 1000;
    return duration_ms;
}
